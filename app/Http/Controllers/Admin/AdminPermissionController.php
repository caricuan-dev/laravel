    /**
     * Show the form for editing the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit($id)
    {
        $data = Permission::join('role_has_permissions','role_has_permissions.permission_id','=','permissions.id')
        ->where('role_has_permissions.role_id','=',$id)
        ->get();
        return response()->json(['details' => $data]);
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $role_id = $request->roles;
        if (DB::table('role_has_permissions')->where('role_id','=',$role_id)->first()) {
            DB::table('role_has_permissions')->where('role_id','=',$role_id)->delete();
        }

        $role_nm = Role::find($role_id);
        
        $permissions = $request->input('perm');
        foreach ($permissions as $key => $permission) {
            $role_nm->givePermissionTo($permission);
        }

        return response()->json(['code' => 1, 'msg' => 'Data Telah Tersimpan!']);
    }

